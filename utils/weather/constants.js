// 获取风向文字
export function getWindDirection(degree) {
  const directions = Object.keys(WIND_DIRECTIONS).map(Number).sort((a, b) => a - b)
  for (let i = 0; i < directions.length; i++) {
    if (degree <= directions[i] + 22.5) {
      return WIND_DIRECTIONS[directions[i]]
    }
  }
  return WIND_DIRECTIONS[0]
} 